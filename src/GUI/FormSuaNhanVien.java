    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        try {
            String selectedChucVu = cboChucVu.getSelectedItem().toString();
            boolean cv = !selectedChucVu.equals("Chưa chọn") ? true : false;
            boolean gioitinh = rboNam.isSelected();
            Date ngaysinh = sdf.parse(txtNgaysinh.getText());
            NhanVien nv = new NhanVien(
                    txtmanhanvien.getText(),
                    txttennhanvien.getText(),
                    ngaysinh,
                    gioitinh,
                    txtdiachi.getText(),
                    txtsdt.getText(),
                    txtEmail.getText(),
                    cv,
                    BigDecimal.valueOf(Double.parseDouble(txtLuong.getText())),
                    null,
                    null
            );
            new NhanVienDao().UpdateSQL(nv);
            NV.LoadData();
            dispose();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ. Vui lòng nhập theo định dạng yyyy-MM-dd.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSuaActionPerformed

