    public void Show(String manv, String hoten, Date Ngaysinh, String gioitinh, String SDT, String Diachi, String chucvu, String Luong, String email) {
        txtmanhanvien.setText(manv);
        txttennhanvien.setText(hoten);
        txtNgaysinh.setText(sdf.format(Ngaysinh));
        txtsdt.setText(SDT);
        txtdiachi.setText(Diachi);
        txtLuong.setText(Luong);
        txtEmail.setText(email);
        if (gioitinh.equalsIgnoreCase("Nam")) {
            rboNam.setSelected(true);
        } else if (gioitinh.equalsIgnoreCase("Nữ")) {
            rboNu.setSelected(true);
        }
        int b = 0;
        if (chucvu.equalsIgnoreCase("Quản Lý")) {
            b = 1;
        } else if (chucvu.equalsIgnoreCase("Nhân viên")) {
            b = 0;
        }
        cboChucVu.setSelectedIndex(b);
    }

